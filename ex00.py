# Este programa vai calcular e informar a area x e y de um quadrado.

# Criando as variaveis que vão fazer o calculo e guardar os resultados e criando a entrada de dados:
area_quad = int(input("Qual a area de um dos lados do quadrado: "))
x = area_quad * 4
y = area_quad ** 2

# Mostrando na tela os resultados dos calculos
print("perímetro:", x, "- área:", y)
