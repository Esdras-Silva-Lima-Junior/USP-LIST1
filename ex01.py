#Um programa que faz um calculo de média das notas inseridas

#Criando as entradas de dados e a variavel que irá fazer e guardar o calculo:
nota1 = float(input("Qual a sua primeira nota: "))
nota2 = float(input("Qual a sua segunda nota: "))
nota3 = float(input("Qual a sua terceira nota: "))
nota4 = float(input("Qual a sua quarta nota: "))
media = (nota1 + nota2 + nota3 + nota4) / 4

#Mostrando na tela o resultado no formato float:
print("A média aritmética é", float(media))
